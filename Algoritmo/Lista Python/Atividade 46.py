def soma(val1,val2):
    som= val1+val2
    sub= val1-val2
    return som,sub
var1=float(input('Digite o 1º número:'))
var2=float(input('Digite o 2º número:'))
print(f'O resultado da soma dos números {var1} e {var2} é: {soma(var1,var2)[0]}\ne o resultado da subtração é: {soma(var1,var2)[1]}')

