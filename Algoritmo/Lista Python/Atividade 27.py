lista= list()
for c in range(1,3):
    lista.append(float(input(f'Digite o {c} numero')))
if lista[0] != lista[1]:
    print('Os 2 numeros são diferentes!')
    print(f'o maior valor é {max(lista)}')
else:
    print('Os numeros são iguais!')

