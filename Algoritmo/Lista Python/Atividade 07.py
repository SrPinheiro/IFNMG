"""7. Faça um algoritmo que receba o preço de custo de um produto e mostre o valor de venda. Sabe-se que o preço
de custo receberá um acréscimo de acordo com um percentual informado pelo usuário."""
var1=float(input('Digite o preço do produto:'))
var2=float(input('Qual a porcentagem de acrescimo?'))
print(f'O valor de venda devera ser de R${var1+(var1*(var2/100))}')

