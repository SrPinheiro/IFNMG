public class Atividade07 {
    public static void main(String[] args) {
        /*7. Alonso decidiu passear pelas cidades próximas da região onde mora. Para conhecer os locais,
ele gastou 2 horas percorrendo uma distância de 120 km. Escreva um programa que calcule e
imprima a velocidade que Alonso estava em seu passeio. */
        double distancia = 120; // Km
        double tempo = 2; // horas

        double delta = distancia/tempo;

        System.out.println("-------------------------------------------------");
        System.out.printf("Velocidade: %.2fKm/h\n", delta);
        System.out.println("-------------------------------------------------");
    }
}
