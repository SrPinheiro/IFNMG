/*1. Escrever um programa para exibir os números de 1 até 50 na tela.*/

public class Atividade01 {
    public static void main(String[] args) {
        for(byte i = 1; i <= 50; i++){
            System.out.printf("Numero: %d\n", i);
        }
        
    }
    
}
